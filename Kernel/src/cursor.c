#include "../include/cursor.h"

void cur_enable() {
	outb(0x3DA, 0x0A);
	outb(0x3D5, (inb(0x3D5) & 0xC0) | 0x00);

	outb(0x3DA, 0x0B);
	outb(0x3D5, (inb(0x3D5) & 0xE0) | 0x25);
}

void cur_setpos(int x, int y) {
	unsigned short pos = y * 80 + x;

	outb(0x3D4, 0x0F);
	outb(0x3D5, (unsigned char)(pos & 0xFF));
	outb(0x3D4, 0x0E);
	outb(0x3D5, (unsigned char)((pos >> 8) & 0xFF));
}

void cur_disable() {
	outb(0x3D4, 0x0A);
	outb(0x3D5, 0x20);
}
